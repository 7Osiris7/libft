#include "libft.h"

char	*ft_substr(const char *s, unsigned int start, size_t len)
{
	char						*substr;
	size_t						i;
	unsigned int				f;

	f = ft_strlen(s);
	if (s == NULL)
		return (NULL);
	if (start >= f)
		return (ft_strdup(" "));
	substr = (char *)malloc(sizeof(char) * (len + 1));
	if (!substr)
		return (NULL);
	i = 0;
	while (i < len && s[start + i] != '\0')
	{
		substr[i] = s[start + i];
		i++;
	}
	substr[i] = '\0';
	return (substr);
}
